class Municipality{

    constructor(){}

    setId(id){ this.id = id; }
    getId(){ return this.id; }

    setName(name){ this.name = name; }
    getName(){ return this.name; }

    setProvince(province){ this.province = province; }
    getProvince(){ return this.province; }

    setDistrict(district){ this.district = district; }
    getDistrict(){ return this.district; }

    setMayor(mayor){ this.mayor = mayor; }
    getMayor(){ return this.mayor; }

    setPopulation(population){ this.population = population; }
    getPopulation(){ return this.population; }

    setMHO(mho){ this.mho = mho; }
    getMHO(){ return this.mho; }

    setPDOH(pdoh){ this.pdoh = pdoh; }
    getPDOH(){ return this.pdoh; }

    setDMO(dmo){ this.dmo = dmo; }
    getDMO(){ return this.dmo; }

    setMunEngr(munEngr){ this.munEngr = munEngr; }
    getMunEngr(){ return this.munEngr; }

    setLGU(LGU){ this.LGU = LGU; }
    getLGU(){ return this.LGU; }

    setPointPerson(pointPerson){ this.pointPerson = pointPerson; }
    getPointPerson(){ return this.pointPerson; }

}

module.exports = Municipality;